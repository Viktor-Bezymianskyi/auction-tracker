import re


def is_excluded(text: str) -> bool:
    """
    Проверяет, нужно ли исключить лот на основе японского описания.
    Возвращает True, если лот содержит ключевые слова для исключения.
    """
    exclude_keywords = [
        # Аксессуары и комплектующие (японские)
        'バッテリー', '充電器', '電池', 'カバー', 'フィルム',
        '保護フィルム', 'シート', 'ケース', 'カバー', 'アクセサリー',
        '交換用', 'パーツ', '部品', '修理用', '補修品',

        # Уточнения что это только аксессуар/часть
        'のみ', 'だけ', '専用', '付属品', '附属品', 'オプション',

        # Книги и руководства
        'マニュアル', '指南', '解説', '方法', 'ガイド', '本',
        '書籍', '出版', 'Kindle本', '電子書籍',

        # Конкретные фразы для исключения
        '箱のみ', '説明書のみ', '充電器のみ', 'ケースのみ',
        'カバーのみ', 'フィルムのみ', '付属品のみ',
        '保護フィルムとガラス', '充電器、ケースのみ',

        # Ремонт и запчасти
        '修理', 'ジャンク', '故障', '分解', '再生品',
        '中古品', '再生部品', '修理キット',

        # Сертификаты и совместимость
        'PSE認証', '適合品', '互換品', '汎用', '互換',
    ]

    # Дополнительные проверки для сложных случаев
    exclusion_patterns = [
        r'【.+のみ】',  # Шаблоны типа 【充電器のみ】
        r'^[Ａ-Ｚa-z0-9-]+$',  # Только коды товаров
        r'[0-9]{4}-[0-9]{3}',  # Коды типа 1234-567
    ]

    # Проверка ключевых слов
    if any(kw in text for kw in exclude_keywords):
        return True

    # Проверка по регулярным выражениям
    if any(re.search(pattern, text) for pattern in exclusion_patterns):
        return True

    return False
