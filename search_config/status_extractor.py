def extract_status(text: str) -> str:
    """
    Определяет статус товара (новый, б/у, дефектный и т.д.) по японскому описанию.

    :param text: Текст описания лота
    :return: Статус на русском языке
    """
    status_map = {
        # Новый
        '新品': 'Новый',
        '未使用': 'Не использовался',
        '未開封': 'Не вскрывался',

        # Б/у
        '中古': 'Б/у',
        '使用済': 'Б/у',
        '美品': 'Б/у (в отличном состоянии)',

        # На запчасти / сломан
        'ジャンク': 'Хлам / на запчасти',
        '訳あり': 'С дефектом / хлам',
        '故障': 'Неисправен',
        '破損': 'Сломан / поврежден',
        '動作未確認': 'Работоспособность не проверена',
        '動作しない': 'Не работает',
        '不良品': 'Дефектный',
        '要修理': 'Требует ремонта',
        '部品取り': 'На запчасти',
        '外装のみ': 'Только корпус',
        '本体のみ': 'Только устройство, без аксессуаров',
        '電源入らず': 'Не включается',

        # Упаковка / комплект
        '箱無し': 'Без коробки',
        '箱破損': 'Повреждённая коробка',
        '付属品なし': 'Без аксессуаров',

        # Оптовое/партия
        'まとめ売り': 'Партия / лот',
        '大量': 'Оптовая партия',
        '業者向け': 'Для продавцов / оптом',
        '卸売': 'Опт',
    }

    for jp_status, ru_status in status_map.items():
        if jp_status in text:
            return ru_status

    return ''  # Если ничего не найдено
